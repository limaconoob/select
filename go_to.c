#include "libft.h"

void go_to(int x, int y)
{ ft_putstr_fd("\033[", 2);
  ft_putnbr_fd(y + 1, 2);
  ft_putchar_fd(';', 2);
  ft_putnbr_fd(x + 1, 2);
  ft_putchar_fd('H', 2); }
